def _norm_col(name: str) -> str:
    return (
        str(name)
        .strip()
        .lower()
        .replace("á", "a")
        .replace("à", "a")
        .replace("ã", "a")
        .replace("â", "a")
        .replace("é", "e")
        .replace("ê", "e")
        .replace("í", "i")
        .replace("ó", "o")
        .replace("õ", "o")
        .replace("ô", "o")
        .replace("ú", "u")
        .replace("ç", "c")
        .replace("/", "_")
        .replace("-", "_")
        .replace(" ", "_")
    )


def _first(row: pd.Series, *names: str, default: Any = None) -> Any:
    normalized = {_norm_col(c): c for c in row.index}
    for name in names:
        key = _norm_col(name)
        if key in normalized:
            value = row[normalized[key]]
            if pd.notna(value):
                return value
    for name in names:
        key = _norm_col(name)
        for normalized_col, original_col in normalized.items():
            if key and key in normalized_col:
                value = row[original_col]
                if pd.notna(value):
                    return value
    return default


def _to_bool_int(value: Any, default: int = 0) -> int:
    if value is None:
        return default
    try:
        if pd.isna(value):
            return default
    except Exception:
        pass
    if isinstance(value, bool):
        return int(value)
    text = str(value).strip().lower()
    if text in {"1", "true", "sim", "yes", "y", "ativo", "concluído", "concluido"}:
        return 1
    if text in {"0", "false", "não", "nao", "no", "n", "inativo"}:
        return 0
    return default


def _clean_excel_value(value: Any) -> Any:
    if value is None:
        return None
    try:
        if pd.isna(value):
            return None
    except Exception:
        pass
    if isinstance(value, pd.Timestamp):
        return value.date().isoformat()
    if isinstance(value, float) and value.is_integer():
        text = str(int(value))
    else:
        text = str(value).strip()
    if text.lower() in {"", "nan", "none", "nat"}:
        return None
    return text


def _normalize_role(value: Any) -> str:
    role = str(_clean_excel_value(value) or "member").strip().lower()
    aliases = {
        "administrador": "admin",
        "admin": "admin",
        "gerente": "manager",
        "manager": "manager",
        "operator": "manager",
        "operador": "manager",
        "membro": "member",
        "member": "member",
        "usuario": "member",
        "usuário": "member",
        "user": "member",
    }
    return aliases.get(role, "member")


