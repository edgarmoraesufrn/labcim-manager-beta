def _row_to_dict(row: Any) -> dict[str, Any]:
    if isinstance(row, dict):
        return dict(row)
    return dict(row)


def query_df(conn: DatabaseConnection, sql: str, params: list[Any] | tuple[Any, ...] | None = None) -> pd.DataFrame:
    cur = conn.execute(sql, params or [])
    columns = [col[0] for col in cur.description or []]
    rows = cur.fetchall()
    if not rows:
        return pd.DataFrame(columns=columns)
    return pd.DataFrame([_row_to_dict(row) for row in rows], columns=columns)


