class DatabaseConnection:
    def __init__(self, raw_conn: Any, dialect: str):
        self.raw_conn = raw_conn
        self.dialect = dialect

    def execute(self, sql: str, params: list[Any] | tuple[Any, ...] | None = None):
        return self.raw_conn.execute(_prepare_sql(self, sql), params or [])

    def executescript(self, sql: str) -> None:
        if self.dialect == "sqlite":
            self.raw_conn.executescript(sql)
            return
        _execute_script(self, sql)

    def commit(self) -> None:
        self.raw_conn.commit()

    def rollback(self) -> None:
        self.raw_conn.rollback()

    def close(self) -> None:
        self.raw_conn.close()


_INTEGRITY_ERRORS: tuple[type[BaseException], ...] = (sqlite3.IntegrityError,)
if psycopg is not None:
    _INTEGRITY_ERRORS = (*_INTEGRITY_ERRORS, psycopg.IntegrityError)


def _prepare_sql(conn: DatabaseConnection, sql: str) -> str:
    if conn.dialect == "postgres":
        sql = sql.replace("?", "%s")
        return re.sub(r"CURRENT_TIMESTAMP(?!::text)", "(CURRENT_TIMESTAMP::text)", sql)
    return sql


def _execute_script(conn: DatabaseConnection, sql: str) -> None:
    for statement in sql.split(";"):
        statement = statement.strip()
        if statement:
            conn.execute(statement)


def _postgres_schema(sqlite_schema: str) -> str:
    return (
        sqlite_schema
        .replace("INTEGER PRIMARY KEY AUTOINCREMENT", "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY")
        .replace("TEXT DEFAULT CURRENT_TIMESTAMP", "TEXT DEFAULT (CURRENT_TIMESTAMP::text)")
        .replace("REAL", "DOUBLE PRECISION")
    )


def db_backend(conn: DatabaseConnection) -> str:
    return conn.dialect


def connect(path: Path | str, database_url: str | None = None) -> DatabaseConnection:
    database_url = database_url or os.environ.get("DATABASE_URL")
    if database_url:
        if psycopg is None or dict_row is None:
            raise RuntimeError("DATABASE_URL foi configurado, mas o driver PostgreSQL psycopg não está instalado.")
        pg_conn = psycopg.connect(database_url, row_factory=dict_row)
        return DatabaseConnection(pg_conn, "postgres")

    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(path)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys = ON")
    return DatabaseConnection(conn, "sqlite")


def _has_column(conn: DatabaseConnection, table: str, column: str) -> bool:
    if db_backend(conn) == "sqlite":
        rows = conn.execute(f"PRAGMA table_info({table})").fetchall()
        return any(r["name"] == column for r in rows)
    row = conn.execute(
        """
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = current_schema()
          AND table_name = ?
          AND column_name = ?
        LIMIT 1
        """,
        [table, column],
    ).fetchone()
    return row is not None


def _add_column(conn: DatabaseConnection, table: str, column: str, definition: str) -> None:
    if not _has_column(conn, table, column):
        conn.execute(f"ALTER TABLE {table} ADD COLUMN {column} {definition}")


